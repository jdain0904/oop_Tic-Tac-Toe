# =============================================================================
# config.py — 학습 전체에서 공유되는 하이퍼파라미터 및 설정값 모음
#
# 이 파일에 숫자를 한 곳에 모아두면,
#   "학습 속도가 너무 빠르다"  → LEARNING_RATE 를 줄이면 됩니다.
#   "탐험을 더 많이 하고 싶다" → EPSILON_START 를 높이면 됩니다.
# 코드 여러 파일을 뒤질 필요 없이 이 파일 하나만 수정하면 됩니다.
# =============================================================================

# ── 보드 설정 ──────────────────────────────────────────────────────────────
# 4×4 틱택토: 4줄 완성 시 승리
# 3×3 대비 상태 수가 수백만 배 증가해 Q-테이블이 점진적으로 성장하며 학습합니다.
BOARD_SIZE = 4    # 가로·세로 칸 수 (4×4 = 16칸)
WIN_LENGTH = 4    # 몇 개를 연속으로 놓으면 이기는지

# ── 학습 횟수 ──────────────────────────────────────────────────────────────
# 4×4는 상태 공간이 3×3보다 수백만 배 넓으므로 에피소드를 크게 늘립니다.
# 1,000,000판 기준으로 ε 이 EPSILON_END 에 도달하도록 EPSILON_DECAY 를 설정합니다.
NUM_EPISODES = 600_000

# ── Q-러닝 핵심 파라미터 ──────────────────────────────────────────────────
# LEARNING_RATE (알파, α):
#   새로운 정보를 기존 지식에 얼마나 빠르게 반영할지 결정합니다.
#   0에 가까울수록 천천히 배우고, 1에 가까울수록 최근 결과에만 의존합니다.
LEARNING_RATE = 0.3

# DISCOUNT_FACTOR (감마, γ):
#   미래 보상을 현재 기준으로 얼마나 할인할지 결정합니다.
#   4×4는 최대 16수까지 이어지므로 3×3보다 먼 미래 가치가 더 중요합니다.
DISCOUNT_FACTOR = 0.95

# ── 탐험(Exploration) vs 활용(Exploitation) ───────────────────────────────
EPSILON_START = 1.0

# EPSILON_END: 학습 후반에도 5% 탐험을 유지해 미방문 상태를 계속 경험합니다.
# 4×4는 상태 공간이 넓어서 탐험을 완전히 끊으면 빈 영역이 너무 많이 남습니다.
EPSILON_END = 0.05

# EPSILON_DECAY: NUM_EPISODES 판 뒤에 EPSILON_END 에 도달하도록 계산합니다.
#   계산: 0.05 = 1.0 × decay^600_000  →  decay ≈ e^(ln(0.05)/600_000) ≈ 0.999995
EPSILON_DECAY = 0.999995

# ── 보상값 (Reward Values) ────────────────────────────────────────────────
# 보상 설계 철학:
#   승리 = 최대 양수, 패배 = 최대 음수, 무승부 = 작은 양수,
#   잘못된 수(이미 채워진 칸) = 음수 패널티
#
# 4×4에서 무승부가 더 자주 발생하므로 REWARD_DRAW 를 3×3과 동일하게 유지합니다.
# "비기는 것이 지는 것보다 낫다"는 신호가 여전히 중요합니다.
REWARD_WIN     =  1.0
REWARD_LOSE    = -1.0
REWARD_DRAW    =  0.3
REWARD_STEP    =  0.0
REWARD_INVALID = -0.5

# ── 모델 저장 경로 ────────────────────────────────────────────────────────
MODEL_DIR    = "models"
MODEL_PATH_O = f"{MODEL_DIR}/agent_O.pkl.gz"
MODEL_PATH_X = f"{MODEL_DIR}/agent_X.pkl.gz"

# ── 로그·출력 주기 ────────────────────────────────────────────────────────
# LOG_INTERVAL 판마다 훈련 통계와 epsilon=0 실력 평가를 함께 출력합니다.
# 훈련 통계(epsilon 포함)만 보면 학습이 정체처럼 보일 수 있지만,
# epsilon=0 평가 수치가 꾸준히 오르면 실제로 학습이 진행되고 있는 것입니다.
LOG_INTERVAL = 100_000
